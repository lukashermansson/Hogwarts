    private void jButtonUppdateraActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonUppdateraActionPerformed
        //förbered id hämning från namn
        String prefektVal = (String)jComboBox1.getSelectedItem();
        String[] prefektNamn = prefektVal.split(" ");
        String lararVal = (String)jComboBox2.getSelectedItem();
        
        String[] lararNamn = lararVal.split(" ");
        try {
            //hitta elevens id
            String prefektID = db.fetchSingle("SELECT ELEV_ID FROM ELEV WHERE FORNAMN = '" + prefektNamn[0] +  "' AND EFTERNAMN = '" + prefektNamn[1] + "'");
        
            //updatera prefekten
            db.update("UPDATE ELEVHEM SET PREFEKT = " + prefektID + " WHERE ELEVHEM_ID = " + elevhemsID + ";");
            
            //hitta lärarens id
            String LarareID = db.fetchSingle("SELECT LARAR_ID FROM LARARE WHERE FORNAMN = '" + lararNamn[0] +  "' AND EFTERNAMN = '" + lararNamn[1] + "'");
        
            //updatera föreståndaren
            db.update("UPDATE ELEVHEM SET FORESTANDARE = " + LarareID + " WHERE ELEVHEM_ID = " + elevhemsID + ";");
            
        } catch (InfException ex) {
            jLabelError.setText("Sql fel:" + ex.getMessage());
        }
        
        
        
    }//GEN-LAST:event_jButtonUppdateraActionPerformed

   

