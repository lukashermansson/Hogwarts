    public AndraElevhemForm(InfDB db, int elevhemsID) {
        this.db = db;
        this.elevhemsID = elevhemsID; 
        
        
        initComponents();
        ArrayList<String> elevLista = new ArrayList<>();
        try {
            //Plocka fram prefektkandidater
            ArrayList<HashMap<String, String>> result = db.fetchRows("SELECT FORNAMN, EFTERNAMN FROM ELEV JOIN SOVSAL ON SOVSAL = SOVSAL_ID WHERE ELEVHEM = " + elevhemsID +  ";");
            
            if(result != null){
                //Populera en temporär arraylist
               for(HashMap<String, String> row : result){
                   elevLista.add(row.get("FORNAMN") + " " + row.get("EFTERNAMN"));
               }
               
               //Populera comboboxen
               for(String elev : elevLista){
                    jComboBox1.addItem(elev);
            
                }
               
               //Välj den tidigare elevhemsprefekt i listan
                HashMap<String, String> tidigareElev = db.fetchRow("SELECT FORNAMN, EFTERNAMN FROM ELEV JOIN ELEVHEM ON PREFEKT = ELEV_ID WHERE ELEVHEM_ID = " + elevhemsID +  ";");
               
                String item;
                if(tidigareElev != null){
                    for (int i = 0; i < jComboBox1.getItemCount(); i++){
                        item = jComboBox1.getItemAt(i);
                        if (item.equals(tidigareElev.get("FORNAMN") + " " + tidigareElev.get("EFTERNAMN"))){
                            jComboBox1.setSelectedIndex(i);
                            break;
                        }
                    }
                }
                
            }
            
        } catch (InfException ex) {
            jLabelError.setText("Sql fel");
        }
        
   
        
        ArrayList<String> lararLista = new ArrayList<>();
        try {
            ArrayList<HashMap<String, String>> result = db.fetchRows("SELECT FORNAMN, EFTERNAMN FROM LARARE;");
            
            if(result != null){
                for(HashMap<String, String> row : result){
                    lararLista.add(row.get("FORNAMN") + " " + row.get("EFTERNAMN"));
                }
                
                for(String larare : lararLista){
                    jComboBox2.addItem(larare);
                }
                
                //Välj den tidigare elevhemsföreståndaren i listan
                HashMap<String, String> tidigareLarare = db.fetchRow("SELECT FORNAMN, EFTERNAMN FROM LARARE JOIN ELEVHEM ON FORESTANDARE = LARAR_ID WHERE ELEVHEM_ID = "+ elevhemsID + ";");
               
                String item;
                if(tidigareLarare != null){
                    for (int i = 0; i < jComboBox2.getItemCount(); i++){
                        item = jComboBox2.getItemAt(i);
                        if (item.equals(tidigareLarare.get("FORNAMN") + " " + tidigareLarare.get("EFTERNAMN"))){
                            jComboBox2.setSelectedIndex(i);
                            break;
                        }
                    }
                }
            }
            
            
        } catch (InfException ex) {
            jLabelError.setText("Sql fel");
        }
        
        
        
        //Hantera fönsterstängning 
        this.setDefaultCloseOperation(JFrame.DO_NOTHING_ON_CLOSE);
    }

